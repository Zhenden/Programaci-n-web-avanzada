<h2>Bienvenido al Sistema de Gestión del Gimnasio</h2>
<p>Utiliza el menú para navegar. Inicia sesión para acceder a las funciones según tu rol (Administrador, Instructor, Miembro).</p>
<ul>
    <li>Administrador: gestionar miembros, clases, instructores e instalaciones.</li>
    <li>Instructor: gestionar sus clases y horarios.</li>
    <li>Miembro: reservar clases y ver su historial.</li>
</ul>